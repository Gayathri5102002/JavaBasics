package basic;

public class Operator {
public static void main (String[] args) {
	int a=20;
	int b=10;
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.println(a*b);
	System.out.println(a/b);
	System.out.println(a%b);
	int c=5;
	c++;
	System.out.println(c);
	c--;
	System.out.println(c);
	c+=10;
	System.out.println(c);
	int x=10;
	x-=10;
	System.out.println(x);
	x*=10;
	System.out.println(x);
	x/=5;
	System.out.println(x);
	x%=5;
	System.out.println(x);
	
	int d=20;
	int e=20;
	System.out.println((d==e)&&(d>e));
	System.out.println((d==e)||(d>e));
	System.out.println(!((d==e)&&(d>e)));
	System.out.println(d==e);
	System.out.println(d!=e);
	System.out.println(d>=e);
	System.out.println(d<=e);
	System.out.println(d>e);
	System.out.println(d<e);

	
	
	
}
}
